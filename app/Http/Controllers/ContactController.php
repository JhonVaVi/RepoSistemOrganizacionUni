    {
        //
        //return $request->all();
        request()->validate(
            Contact::$rules
        );
        Contact::create([
            'name'=>$request->name,
            'surname'=>$request->surname,
            'email'=>$request->email,
            'number'=>$request->number,
            'user_id'=>Auth::user()->id,
        ]);
        return redirect()->route('contacts.index')->with('success', 'Contacto creado con Ã©xito');

